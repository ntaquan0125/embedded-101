/* USER CODE BEGIN PFP */
void WX281x_init(void)
{
	for (uint16_t i = DELAY_RES; i < DMA_BUF_SIZE; i++)
	{
		dma_buf[i] = LOW;
	}
}

void WX281x_update(void)
{
	for (uint8_t i = 0; i < LED_COUNT; i++)
	{
		for (uint8_t b = 0; b < 8; b++)
		{
			dma_buf[DELAY_RES + i * 24 + b] = RESOLVE_BIT_TIME(led_buf[i].G, b);
			dma_buf[DELAY_RES + i * 24 + b + 8] = RESOLVE_BIT_TIME(led_buf[i].R, b);
			dma_buf[DELAY_RES + i * 24 + b + 16] = RESOLVE_BIT_TIME(led_buf[i].B, b);
		}
	}
}

void set_LED(uint8_t i, uint8_t R, uint8_t G, uint8_t B)
{
	led_buf[i].R = R;
	led_buf[i].G = G;
	led_buf[i].B = B;
}

void set_all_LED(uint8_t R, uint8_t G, uint8_t B)
{
	for (uint8_t i = 0; i < LED_COUNT; i++)
	{
		set_LED(i, R, G, B);
	}
}

void WX281x_send(void)
{
	WX281x_update();
	HAL_TIM_PWM_Start_DMA(&htim2, TIM_CHANNEL_1, (uint32_t *)dma_buf, DMA_BUF_SIZE);
	set_all_LED(0, 0, 0);
}
